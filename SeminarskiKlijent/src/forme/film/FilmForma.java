    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        try {
            String naziv = jTextFieldNazivFilma.getText();
            if (naziv.isEmpty()) {
                throw new Exception("Niste uneli naziv filma");
            }
            int brojPrimeraka = Integer.parseInt(jTextFieldBrojPrimeraka.getText());
            if (brojPrimeraka <= 0) {
                throw new Exception("Niste uneli validan broj primeraka");
            }
            String polica = jTextFieldPolica.getText();
            if (polica.isEmpty()) {
                throw new Exception("Niste uneli policu");
            }
            int godina = Integer.parseInt(jTextFieldGodina.getText());
            if (godina <= 0) {
                throw new Exception("Niste uneli validnu godinu");
            }
            Reziser reziser=null;
            for (Reziser r : reziseri) {
                if (r.toString().equals(jComboBoxReziser.getSelectedItem())) {
                    reziser = r;
                }
            }
            Zanr zanr=null;

            for (Zanr z : zanrovi) {
                if (z.toString().equals(jComboBoxZanr.getSelectedItem())) {
                    zanr = z;
                }

            }

            film.setBrojPrimeraka(brojPrimeraka);
            film.setGodina(godina);
            film.setPolica(polica);
            film.setNaziv(naziv);
            film.setZanr(zanr);
            film.setReziser(reziser);

            if (film.getIdFilma() > 0) {
                ClientController.getInstanca().izmeniFilm(film);
                JOptionPane.showMessageDialog(this, "Uspesno ste izmenili film");
            } else {
                ClientController.getInstanca().zapamtiFilm(film);
                JOptionPane.showMessageDialog(this, "Uspesno ste dodali film");

            }
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            ex.printStackTrace();
       }    }//GEN-LAST:event_jButtonSacuvajActionPerformed

