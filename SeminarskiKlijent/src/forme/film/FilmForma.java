    private void napuniComboBoxRezisera() {
        try {
            reziseri = ClientController.getInstanca().ucitajListuRezisera();
            jComboBoxReziser.removeAllItems();
            for (Reziser r : reziseri) {
                jComboBoxReziser.addItem(r.toString());
            }
        } catch (Exception ex) {
            Logger.getLogger(FilmForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void napuniComboBoxZanrova() {
        try {
            zanrovi = ClientController.getInstanca().ucitajListuZanrova();
            jComboBoxZanr.removeAllItems();
            for (Zanr zanr : zanrovi) {
                jComboBoxZanr.addItem(zanr.toString());
            }
        } catch (Exception ex) {
            Logger.getLogger(FilmForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
